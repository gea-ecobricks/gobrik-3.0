            submitPasswordButton.style.opacity = '0';
            submitPasswordButton.classList.add('hidden');
            setTimeout(() => {
                sendCodeButton.classList.remove('hidden');
                sendCodeButton.style.opacity = '1';
            }, 1000); // 1 second delay
        }
    }

